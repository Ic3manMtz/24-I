public class Artimeticos {
  public static void main(String[] args) {
    int a, b;
    a=20;
    b=10;

    System.out.println("20 + 10 = "+ (a+b));
    System.out.println("20 - 10 = "+ (a-b));
    System.out.println("20 * 10 = "+ (a*b));
    System.out.println("20 / 10 = "+ (a/b));
    System.out.println("20 % 10 = "+ (a%b));
  }
}
